from __future__ import annotations

from typing import Any


def normalize_text(value: Any) -> str:
    # ??????? ??????????? ? ???????? ? ??????? ???????? ??? ??????? ?????????
    return "".join(ch for ch in str(value or "").lower() if ch.isalnum())


def dedup_key(v: dict[str, Any]) -> str:
    # ???? ????????????: ???????? + ????????
    title = normalize_text(v.get("title"))
    company = normalize_text(v.get("company"))
    if title and company:
        return f"tc_{title}_{company}"

    vid = str(v.get("id") or "").strip()
    if vid:
        return f"id_{vid}"

    link = str(v.get("link") or "").strip().lower()
    if link:
        return f"link_{link}"

    # ??????? ??????: ??? ???? ?????
    return f"fallback_{hash(str(sorted(v.items())))}"


def merge_vacancies(
    gd_mail: list[dict[str, Any]],
    gd_scrape: list[dict[str, Any]],
    li_data: list[dict[str, Any]],
) -> tuple[list[dict[str, Any]], int]:
    all_rows = []
    # ???????????? ?????????? ????? ????????
    for v in gd_mail:
        row = dict(v)
        row["source"] = row.get("source") or "glassdoor"
        all_rows.append(row)
    for v in gd_scrape:
        row = dict(v)
        row["source"] = row.get("source") or "glassdoor"
        all_rows.append(row)
    for v in li_data:
        row = dict(v)
        row["source"] = row.get("source") or "linkedin"
        all_rows.append(row)

    unique: dict[str, dict[str, Any]] = {}
    duplicates_removed = 0

    for v in all_rows:
        k = dedup_key(v)
        existing = unique.get(k)
        if existing is None:
            unique[k] = v
            continue

        duplicates_removed += 1
        
        # ?????? ???????????:
        # LinkedIn ?????? ????????? ????? ?????????????, ??? Glassdoor
        if existing.get("source") == "glassdoor" and v.get("source") == "linkedin":
            unique[k] = v
            continue

        # ???? ???????? ??? ??, ?? ? ????? ?????? ???? ID, ? ? ?????? ??? ? ?????????
        if (existing.get("source") == v.get("source")) and (not existing.get("id")) and v.get("id"):
            unique[k] = v

    return list(unique.values()), duplicates_removed
