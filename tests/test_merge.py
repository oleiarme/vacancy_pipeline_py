def test_merge_linkedin_wins_on_collision():
    # ????????: ???? ? ?? ?? ???????? ? Glassdoor (?? ?????) ? ? LinkedIn
    gd_mail = [
        {
            "id": "gd_1",
            "title": "DevOps Engineer",
            "company": "Acme",
            "link": "https://gd.example/1",
            "source": "glassdoor",
        }
    ]
    gd_scrape = []
    li_data = [
        {
            "id": "li_999",
            "title": "DevOps Engineer",
            "company": "Acme",
            "link": "https://li.example/999",
            "source": "linkedin",
            "description": "richer data",
        }
    ]

    merged, dup = merge_vacancies(gd_mail, gd_scrape, li_data)
    
